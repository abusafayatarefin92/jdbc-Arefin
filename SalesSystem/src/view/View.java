    private void getCategoryData() {
        List<Category> list = CategoryService.getTableData();
        DefaultTableModel model = (DefaultTableModel) jTableCategory.getModel();
        model.setRowCount(0);
        Object[] row = new Object[2];

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getName();

            model.addRow(row);
        }
    }

    private void getProductData() {
        List<Product> list = ProductService.getProductData();
        DefaultTableModel model = (DefaultTableModel) jTableProduct.getModel();
        model.setRowCount(0);
        Object[] row = new Object[7];

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getQuantity();
            row[3] = list.get(i).getUnitPrice();
            row[4] = list.get(i).getTotalPrice();
            row[5] = list.get(i).getPurchaseDate();
            row[6] = list.get(i).getCategory().getId();

            model.addRow(row);
        }
    }

