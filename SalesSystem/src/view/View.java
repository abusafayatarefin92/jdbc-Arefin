    private DefaultComboBoxModel comboBoxModel = null;

    public void comboBoxItemFromDataBase() {
        comboBoxModel = new DefaultComboBoxModel();
        List<Category> categoryList = CategoryService.getTableData();
        comboBoxModel.addElement("Select A catagory");

        for (Category c : categoryList) {
            comboBoxModel.addElement(c.getId() + " " + c.getName());
        }

        jComboBoxCategory.setModel(comboBoxModel);
    }

